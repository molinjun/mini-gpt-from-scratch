def presentation_rels(slides: list[dict]) -> str:
    rels = []
    for i in range(1, len(slides) + 1):
        rels.append(
            f'<Relationship Id="rId{i}" '
            'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/slide" '
            f'Target="slides/slide{i}.xml"/>'
        )
    rels.append(
        f'<Relationship Id="rId{len(slides)+1}" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/slideMaster" '
        'Target="slideMasters/slideMaster1.xml"/>'
    )
    rels.append(
        f'<Relationship Id="rId{len(slides)+2}" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/notesMaster" '
        'Target="notesMasters/notesMaster1.xml"/>'
    )
    return (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n'
        '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">\n'
        + "\n".join(rels)
        + "\n</Relationships>"
    )


def color_fill(color: str, alpha: int | None = None) -> str:
    alpha_xml = f'<a:alpha val="{alpha}"/>' if alpha is not None else ""
    return f'<a:solidFill><a:srgbClr val="{color}">{alpha_xml}</a:srgbClr></a:solidFill>'


def line_fill(color: str, alpha: int | None = None, width: int = 12700) -> str:
    return f'<a:ln w="{width}">{color_fill(color, alpha)}</a:ln>'


def shape_rect(
    shape_id: int,
    name: str,
    x: int,
    y: int,
    cx: int,
    cy: int,
    fill: str,
    line: str | None = None,
    geom: str = "rect",
) -> str:
    line_xml = line if line is not None else '<a:ln><a:noFill/></a:ln>'
    return f'''<p:sp>
        <p:nvSpPr><p:cNvPr id="{shape_id}" name="{xml_text(name)}"/><p:cNvSpPr/><p:nvPr/></p:nvSpPr>
        <p:spPr>
          <a:xfrm><a:off x="{x}" y="{y}"/><a:ext cx="{cx}" cy="{cy}"/></a:xfrm>
          <a:prstGeom prst="{geom}"><a:avLst/></a:prstGeom>
          {fill}
          {line_xml}
        </p:spPr>
      </p:sp>'''


def text_box(
    shape_id: int,
    name: str,
    text: str,
    x: int,
    y: int,
    cx: int,
    cy: int,
    size: int,
    color: str = "F5F9FF",
    bold: bool = False,
    anchor: str = "t",
    align: str | None = None,
) -> str:
    bold_xml = ' b="1"' if bold else ""
    align_xml = f'<a:pPr algn="{align}"/>' if align else "<a:pPr/>"
    return f'''<p:sp>
        <p:nvSpPr><p:cNvPr id="{shape_id}" name="{xml_text(name)}"/><p:cNvSpPr txBox="1"/><p:nvPr/></p:nvSpPr>
        <p:spPr>
          <a:xfrm><a:off x="{x}" y="{y}"/><a:ext cx="{cx}" cy="{cy}"/></a:xfrm>
          <a:prstGeom prst="rect"><a:avLst/></a:prstGeom>
          <a:noFill/>
          <a:ln><a:noFill/></a:ln>
        </p:spPr>
        <p:txBody>
          <a:bodyPr wrap="square" anchor="{anchor}"/>
          <a:lstStyle/>
          <a:p>
            {align_xml}
            <a:r><a:rPr lang="en-US" sz="{size}"{bold_xml}>{color_fill(color)}<a:latin typeface="Aptos"/></a:rPr><a:t>{xml_text(text)}</a:t></a:r>
          </a:p>
        </p:txBody>
      </p:sp>'''


