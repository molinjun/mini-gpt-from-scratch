def write_pptx(slides: list[dict]) -> None:
    missing = [
        str(image_path(slide))
        for slide in slides
        if slide["n"] != 1 and not image_path(slide).exists()
    ]
    if missing:
        print("Missing slide images:", file=sys.stderr)
        for path in missing:
            print(f"- {path}", file=sys.stderr)
        sys.exit(1)

    if OUT.exists():
        timestamp = datetime.now().strftime("%Y%m%d-%H%M%S")
        backup = OUT.with_name(f"{OUT.stem}-backup-{timestamp}{OUT.suffix}")
        OUT.replace(backup)
        print(f"Backed up existing PPTX to {backup}")

    with zipfile.ZipFile(OUT, "w", compression=zipfile.ZIP_DEFLATED) as pptx:
        pptx.writestr("[Content_Types].xml", content_types(slides))
        pptx.writestr("_rels/.rels", root_rels())
        pptx.writestr("docProps/app.xml", app_xml(slides))
        pptx.writestr("docProps/core.xml", core_xml())
        pptx.writestr("ppt/presentation.xml", presentation_xml(slides))
        pptx.writestr("ppt/_rels/presentation.xml.rels", presentation_rels(slides))
        pptx.writestr("ppt/slideMasters/slideMaster1.xml", slide_master_xml())
        pptx.writestr("ppt/slideMasters/_rels/slideMaster1.xml.rels", slide_master_rels())
        pptx.writestr("ppt/slideLayouts/slideLayout1.xml", slide_layout_xml())
        pptx.writestr("ppt/slideLayouts/_rels/slideLayout1.xml.rels", slide_layout_rels())
        pptx.writestr("ppt/notesMasters/notesMaster1.xml", notes_master_xml())
        pptx.writestr("ppt/notesMasters/_rels/notesMaster1.xml.rels", notes_master_rels())
        pptx.writestr("ppt/theme/theme1.xml", theme_xml())

        for idx, slide in enumerate(slides, start=1):
            if idx == 1:
                pptx.writestr(f"ppt/slides/slide{idx}.xml", cover_slide_xml())
                pptx.writestr(f"ppt/slides/_rels/slide{idx}.xml.rels", cover_slide_rels(idx))
            else:
                pptx.writestr(f"ppt/slides/slide{idx}.xml", slide_xml(idx, slide["title"]))
                pptx.writestr(f"ppt/slides/_rels/slide{idx}.xml.rels", slide_rels(idx))
            pptx.writestr(f"ppt/notesSlides/notesSlide{idx}.xml", notes_xml(slide))
            pptx.writestr(f"ppt/notesSlides/_rels/notesSlide{idx}.xml.rels", notes_rels(idx))
            if idx != 1:
                pptx.write(image_path(slide), f"ppt/media/{media_name(idx)}")


def main() -> None:
    slides = load_slides()
    write_pptx(slides)
    print(f"Created {OUT}")
    print(f"Slides: {len(slides)}")
    print(f"Slides with notes: {len(slides)}")


if __name__ == "__main__":
    main()
