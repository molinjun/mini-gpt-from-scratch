ROOT = Path(__file__).resolve().parents[1]
DECK_DIR = ROOT / "share" / "deck"
OUT = DECK_DIR / "build-mini-gpt-from-scratch.pptx"
TMP_JSON = ROOT / "tmp" / "microgpt_slides.json"

SLIDE_CX = 12192000
SLIDE_CY = 6858000


def load_slides() -> list[dict]:
    TMP_JSON.parent.mkdir(parents=True, exist_ok=True)
    js = (
        "import('./tools/microgpt_deck_data.mjs').then(m=>"
        "process.stdout.write(JSON.stringify(m.slides)))"
    )
    result = subprocess.run(
        ["node", "-e", js],
        cwd=ROOT,
        check=True,
        text=True,
        stdout=subprocess.PIPE,
    )
    return json.loads(result.stdout)


def two(n: int) -> str:
    return f"{n:02d}"


def media_name(index: int) -> str:
    return f"image{index}.png"


def image_path(slide: dict) -> Path:
    return DECK_DIR / f"{two(slide['n'])}-slide-{slide['slug']}.png"


def xml_text(text: str) -> str:
    return escape(text, {"'": "&apos;", '"': "&quot;"})


def para(text: str) -> str:
    text = xml_text(text)
    return (
        '<a:p><a:pPr marL="0" indent="0"/>'
        f'<a:r><a:rPr lang="en-US" sz="1600"/><a:t>{text}</a:t></a:r>'
        "</a:p>"
    )


def notes_paragraphs(notes: list[str]) -> str:
    paragraphs: list[str] = []
    for block in notes:
        for line in re.split(r"\n+", block):
            line = line.strip()
            if line:
                paragraphs.append(para(line))
    return "".join(paragraphs)


